#include <stdio.h>
int main(void)
{
int val = 0;

printf("The value of val: %d\n", val);
printf("The value of val: %d\n", val++);
printf("The value of val: %d\n", val);
printf("The value of val: %d\n", ++val);
printf("The value of val: %d\n", val);
printf("The value of val: %d\n", val--);
printf("The value of val: %d\n", val);
printf("The value of val: d\n", --val);
printf("The value of val: %d\n", val);

return 0;
}
